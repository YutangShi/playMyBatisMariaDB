package pojo.game;

public class Player {
	
	// 遊戲角色姓名
	private String name;
	
	// 性別
	private String sex;
	
	// 種族
	private String race;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getRace() {
		return race;
	}

	public void setRace(String race) {
		this.race = race;
	}

}
